class DummyKLField(KarhunenLoeve):
    """Dummy Karhunen-Loeve random field."""

    def expanded_representation(self, samples):
        """Dummy method for expansion."""
        return self.mean + self.std**2 * np.linalg.norm(self.coords["coords"], axis=1) * samples[0]


def prepare_material_case(case_name, tmp_path, third_party_inputs):
    """Handle input file setup."""
    material_file_template = tmp_path / "material.json"

    if case_name == "yaml":
        fourc_input_template = (
            third_party_inputs / "fourc" / "coarse_plate_dirichlet_template.4C.yaml"
        )

        mue_rf_parameters = create_random_elemenentwise_material_field(
            fourc_input_template,
            "STRUCTURE ELEMENTS",
            extract_by_material_id(10),
            "MUE",
            material_file_template,
        )

        files_to_copy = []

    elif case_name == "exo":
        fourc_input_template = (
            third_party_inputs / "fourc" / "coarse_plate_dirichlet_with_exo_template.4C.yaml"
        )
        mesh_file = third_party_inputs / "fourc" / "membrane_20.e"

        mesh = pv.read(mesh_file)[0][0]
        centers = mesh.cell_centers().points

        mue_rf_parameters = {
            "coords": centers,
            "keys": [f"MUE_{i}" for i in range(1, 1 + len(centers))],
        }

        create_jinja_json_template(
            "MUE",
            np.arange(1, len(centers) + 1),
            mue_rf_parameters["keys"],
            material_file_template,
        )

        files_to_copy = [mesh_file]

    else:
        raise ValueError(f"Unknown case: {case_name}")

    return fourc_input_template, material_file_template, mue_rf_parameters, files_to_copy


@pytest.mark.parametrize(
    "case_name, expected_mean_fixture",
    [
        ("yaml", "expected_mean_yaml"),
        ("exo", "expected_mean_exo"),
    ],
)
def test_write_random_elementwise_material(
    case_name,
    tmp_path,
    third_party_inputs,
    fourc_link,
    expected_mean_fixture,
    request,
    global_settings,
):
    """Test 4C with random field for material parameters."""
    expected_mean = request.getfixturevalue(expected_mean_fixture)
    (
        fourc_input_template,
        material_file_template,
        mue_rf_parameters,
        files_to_copy,
    ) = prepare_material_case(case_name, tmp_path, third_party_inputs)

    # Parameters
    mue = DummyKLField(
        corr_length=5.0,
        std=0.03,
        mean=0.25,
        explained_variance=0.95,
        coords=mue_rf_parameters,
    )
    parameters = Parameters(MUE=mue)

    # Setup iterator
    data_processor = PvdFile(
        field_name="displacement",
        file_name_identifier="output-structure.pvd",
        file_options_dict={},
    )

    scheduler = Local(num_procs=1, num_jobs=1, experiment_name=global_settings.experiment_name)
    driver = Fourc(
        parameters=parameters,
        input_templates={
            "input_file": fourc_input_template,
            "material_file": material_file_template,
        },
        executable=fourc_link,
        data_processor=data_processor,
        files_to_copy=files_to_copy,
    )
    model = Simulation(scheduler=scheduler, driver=driver)
    iterator = MonteCarlo(
        seed=1,
        num_samples=1,
        result_description={"write_results": True, "plot_results": False},
        model=model,
        parameters=parameters,
        global_settings=global_settings,
    )

    try:
        # Actual analysis
        run_iterator(iterator, global_settings=global_settings)

        # Load results
        results = load_result(global_settings.result_file(".pickle"))

        # Check if we got the expected results
        np.testing.assert_array_almost_equal(results["mean"], expected_mean, decimal=8)
    except Exception as error:
        experiment_dir, _ = experiment_directory(global_settings.experiment_name)
        job_dir = experiment_dir / "0"
        _logger.info(list(job_dir.iterdir()))
        output_dir = job_dir / "output"
        _logger.info(list(output_dir.iterdir()))
        _logger.info(read_file(output_dir / "test_write_random_material_to_dat_0.log"))
        raise error


